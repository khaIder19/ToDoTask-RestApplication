package com.todotask.json.user;

public class UserItem {

	private String name;
	private String email;
	
	public UserItem(String name, String email) {
		super();
		this.name = name;
		this.email = email;
	}
	
	public String getName() {
		return name;
	}
	public String getEmail() {
		return email;
	}
	
	
	
}
